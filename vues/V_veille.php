                                    <div class="timeline-body collapse multi-collapse" id="consultation-cameras-intelligentes">
                                        <p>
                                            Les dispositifs concernés par cette consultation sont les <strong>logiciels de traitements automatisés d’images couplés à des caméras</strong>.
                                            <br>Ils permettent en effet d’extraire diverses informations à partir des flux vidéo qui en sont issus.
                                            <br>Ces dispositifs remettent en effet en question nos <strong>libertés individuelles</strong>.
                                            <br>La CNIL souhaite présenter de manière pratique et concrète ce que sont les outils de vidéo « intelligente » ou « augmentée » et leur variété d’usages.
                                            <br>La protection des droits et libertés fondamentaux des personnes filmées doit rester une priorité. L'objectif est donc de faire <strong>respecter l’ensemble de la réglementation applicable</strong> en matière de données personnelles.
                                            <br>L'objectif est de <strong>mettre en avant les enjeux éthiques et sociétaux</strong> de cette technologie et les risques gradués pour les droits et libertés des personnes.
